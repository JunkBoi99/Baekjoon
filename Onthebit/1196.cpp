#include <cstdio>

int map[19][19],n,x;

void reverse_(int x)
{
    for(int p=0;p<19;p++)
    {
        if(map[p][x]==0) map[p][x]=1;
        else map[p][x]=0;
        if(map[x][p]==0) map[x][p]=1;
        else map[x][p]=0;
    }  
}

int main()
{
    for(int i = 0;i<19;i++)
    {
        for(int j = 0;j<19;j++)
            scanf("%d",&map[i][j]);
    }
    scanf("%d",&n);
    for(int i = 0;i<n;i++)
    {
        scanf("%d",&x);
        reverse_(x);
    }
    for(int i = 0;i<19;i++)
    {
        for(int j = 0;j<19;j++)
            printf("%d ",map[i][j]);
        printf("\n");
    }   
}
